num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

add = num1 + num2
sub = num1 - num2
div = num1 / num2
mod = num1 % num2
mult = num1 * num2

print("The total between the first number and second number is " + str(add) + ".")
print("The difference between the first number and second number is " + str(sub) + ".")
print("The quotient between the first number and second number is " + str(div) + " with remainder " + str(mod) + ".")
print("The product between the first number and second number is " + str(mult) + ".")
